			<h3>Flowchart</h3>
			<p>
				A flowchart is a graphical representation of a program. It shows the logical sequence of instruction which a computer has to follow. The individual instructions in the flowchart are connected by arrow lines. These lines indicate the order on how the instructions are followed or executed by the computer. The major users of flowchart are in program documentation and program presentation.

			</p>
			<h3>Two types of Flowchart</h3>
			<ol class="mt-3">
				<li><b>System flowchart</b> – this type of flowchart illustrates which data is used and how it interacts with the hardware and the user.
				</li>
				<li><b>Program flowchart</b> – which illustrates graphically the logical sequence of the program.
				</li>
			</ol>
			<p>
				The flowchart can help us to view the relationships between the two or more parts and subparts of the program. How they relate or interact to each other. Moreover, it improves the clarity of our logical thoughts, since we had seen its relationships to each other in graphical form. The computer program involves the three basic main operations:
			</p>
			<ul>
				<li>
					<b>Input</b>		(to enter data)
				</li>
				<li>
					<b>Process</b>	(to calculate or make choices)
				</li>
				<li>
					<b>Output</b>		(to produce a result or report)
				</li>
			</ol>

